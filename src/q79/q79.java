package q79;

class Solution {
    public boolean exist(char[][] board, String word) {
        char[] array = word.toCharArray();
        for(int i = 0;i < board.length;i++) {
            for(int j = 0;j < board[0].length;j++) {
                if(dfs(board, array, i, j, 0)) return true;
            }
        }
        return false;
    }

    private boolean dfs(char[][] board, char[] word, int l, int r, int depth) {
        if(depth == word.length) return true;
        if(l < 0 || l >= board.length || r < 0 || r >= board[0].length || board[l][r] != word[depth]) {
            return false;
        }

        char tmp = board[l][r];
        board[l][r] = '0';

        boolean rs = dfs(board, word, l-1, r, depth+1) || dfs(board, word, l+1,r,depth+1)
                || dfs(board, word, l,r-1,depth+1) ||dfs(board,word,l,r+1,depth+1);

        board[l][r] = tmp;
        return rs;
    }
}
